/**
 * Obtiene objetos de celda parseados para un nivel
 */
export function getStaticCells(level) {
  switch (level) {
    case 'LOW':
      return parseCellIds(ERA5_CELLS, 0.25);
    case 'MEDIUM':
      return parseCellIds(IMERG_CELLS, 0.1);
    case 'HIGH':
      return parseCellIds(CHIRPS_CELLS, 0.05);
    default:
      return [];
  }
}

/**
 * Obtiene todas las celdas estáticas
 */
export function getAllStaticCells() {
  return {
    LOW: getStaticCells('LOW'),
    MEDIUM: getStaticCells('MEDIUM'),
    HIGH: getStaticCells('HIGH'),
  };
}
